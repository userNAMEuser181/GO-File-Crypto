package test

import (
	"encoding/base64"
	"testing"

	FCLib "github.com/userNAMEuser181/GO-File-Crypto/File_Crypto_Library"
)

func TestCipherModify(t *testing.T) {
	file_key := "1fQf0ufYSEAc9/mBh9IXYYDFPcEMtLUB9es0dwSXKZg="
	key_blob, err := base64.StdEncoding.DecodeString(file_key)
	if err != nil {
		return
	}

	aes_key := FCLib.AES_Key{
		Key: key_blob,
	}

	f_e_content := []byte{
		0xc0, 0xf0, 0xc4, 0x78, 0x02, 0x18, 0xa8, 0x73, 0x6a, 0x44, 0x6f, 0xda, 0xe1, 0x41, 0xaf, 0x09,
		0x84, 0x00, 0x10, 0x00, 0x00, 0xd8, 0x2a, 0x39, 0x00, 0x76, 0xa1, 0x5b, 0x76, 0x9a, 0x17, 0x1d,
		0x4b, 0x05, 0x94, 0x3e, 0x3a, 0x57, 0xd7, 0x12, 0xc3, 0x90, 0x2d, 0x8f, 0x4a, 0x4f, 0xe8, 0x95,
		0x4e, 0x98, 0xd2, 0x2e, 0xad, 0xd6, 0xdb, 0x2c, 0xed, 0x06, 0xd3, 0xff, 0x2d, 0xe7, 0x09, 0x89,
		0x06, 0xca, 0x5b, 0x5a, 0xb8, 0x97, 0x27, 0xaa, 0xb6, 0xc1, 0x98,
	}

	cryptedFile := FCLib.Crypt_File{
		Path:   "./test_cipher.bin",
		AESkey: aes_key,
	}

	// Modifying cipher at postion 19 to 0x27
	f_e_content[55] = 0x27

	run_decryption(t, f_e_content, cryptedFile)
}
